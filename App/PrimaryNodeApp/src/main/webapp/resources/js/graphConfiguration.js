function skinBar() {
    this.cfg.axes.yaxis.tickOptions = {
        textColor: '#dedede'
    };
    this.cfg.axes.yaxis.labelOptions = {
        textColor: '#dedede'
    };
    this.cfg.axes.xaxis.tickOptions = {
        textColor: '#dedede',
        angle: -45
    };
    this.cfg.axes.xaxis.labelOptions = {
        textColor: '#dedede'
    };
    this.cfg.grid = {
        gridLineColor: '#000000',
        background: '#585858'
    };
}